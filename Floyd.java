import java.util.Arrays;

public class Floyd {

    static final int INF = 1_000_000_000;

    public static void floyd(int[][] g) {
        int n = g.length;
        int[][] dist = new int[n][n];

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                if (g[i][j] == 0 && i != j)
                    dist[i][j] = INF;
                else
                    dist[i][j] = g[i][j];
            }
        }

        for (int k = 0; k < n; k++) {
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < n; j++) {
                    if (dist[i][k] + dist[k][j] < dist[i][j]) {
                        dist[i][j] = dist[i][k] + dist[k][j];
                    }
                }
            }
        }

        for (int[] row : dist) {
            System.out.println(Arrays.toString(row));
        }
    }

    public static void main(String[] args) {

        int[][] G = {
            {0,2,7,5,0,0,1,0},
            {2,0,0,0,4,0,0,0},
            {7,0,0,0,3,5,9,0},
            {5,0,0,0,6,4,0,0},
            {0,4,3,6,0,3,0,0},
            {0,0,5,4,3,0,0,1},
            {1,0,9,0,0,0,0,6},
            {0,0,0,0,0,1,6,0}
        };

        floyd(G);
    }
}


